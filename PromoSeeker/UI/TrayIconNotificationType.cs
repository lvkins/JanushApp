namespace PromoSeeker
{
    /// <summary>
    /// Defines a set of <see cref="TrayIcon"/> tooltip notification types.
    /// </summary>
    public enum TrayIconNotificationType
    {
        /// <summary>
        /// A default type.
        /// </summary>
        None = 0,

        /// <summary>
        /// A informative type.
        /// </summary>
        Info = 1,

        /// <summary>
        /// A warning type.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// A error type.
        /// </summary>
        Error = 3
    }
}
