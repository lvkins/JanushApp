        /// A default type.
        /// </summary>
        None = 0,

        /// <summary>
        /// A informative type.
        /// </summary>
        Info = 1,

        /// <summary>
        /// A warning type.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// A error type.
        /// </summary>
        Error = 3
    }
}
